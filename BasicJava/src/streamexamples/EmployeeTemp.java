package streamexamples;

/**
 * @author rutpatel
 *
 */
public class EmployeeTemp {

	private String name;
	private double salary;

	public EmployeeTemp(String name, double salary) {
		this.name = name;
		this.salary = salary;
	}

	public String getName() {
		return name;
	}

	public double getSalary() {
		return salary;
	}
}
